/**
 * @description Clase PersonaDTO que contiene la informacion de una persona
 * 
 * @author Tulio Estrella <tulfer01@gmail.com>
 */
export class PersonaDTO {

    /**
    * Atributo que contiene el nombre de la persona.
    */
    public nombre: string;

    /**
    * Atributo que contiene el tipo de documento de la persona.
    */
    public tipoDocumento: string;

    /**
    * Atributo que contiene el documento de la persona.
    */
    public documento: string;

    /**
    * Atributo que contiene la fecha de nacimiento de la persona.
    */
    public fechaNacimiento: Date;

  
}